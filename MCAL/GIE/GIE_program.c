#include "GIE_interface.h"

void GIE_voidEnableGlobalInterrupt(void)
{
	SET_BIT(SREG, SREG_I);
}

void GIE_voidDisableGlobalInterrupt(void)
{
	CLR_BIT(SREG, SREG_I);
}
